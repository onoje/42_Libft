#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*new_s;

	if (s == NULL || f == NULL)
	{
		return (NULL);
	}
	new_s = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (new_s == 0)
	{
		return (0);
	}
	i = 0;
	while (s[i] != '\0')
	{
		new_s[i] = f(i, s[i]);
		i++;
	}
	new_s[i] = '\0';
	return (new_s);
}
